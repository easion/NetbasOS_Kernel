#define OS_ENTER_CRITICAL()	\
__asm__(			\
	"mrs	r0,	cpsr\n"\
	"stmdb	sp!,	{r0}\n"\
	"orr	r0, r0, 	#0x80\n"\
	"msr	cpsr_c,	r0\n"\
